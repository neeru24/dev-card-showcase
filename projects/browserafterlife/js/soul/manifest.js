export class SoulManifest {
    constructor(seed) {
        this.seed = seed;
        this.rng = this.mulberry32(seed); // Seeded RNG

        this.identity = this.generateIdentity();
        this.biography = this.generateBiography();
        this.stats = this.generateStats();
    }

    // Seeded Random Number Generator
    mulberry32(a) {
        return function () {
            var t = a += 0x6D2B79F5;
            t = Math.imul(t ^ t >>> 15, t | 1);
            t ^= t + Math.imul(t ^ t >>> 7, t | 61);
            return ((t ^ t >>> 14) >>> 0) / 4294967296;
        }
    }

    rand(arr) {
        return arr[Math.floor(this.rng() * arr.length)];
    }

    generateIdentity() {
        return {
            firstName: this.rand(MANIFEST_DATA.firstNames),
            lastName: this.rand(MANIFEST_DATA.lastNames),
            epithet: this.rng() > 0.7 ? this.rand(MANIFEST_DATA.epithets) : "",
            archetype: this.determineArchetype()
        };
    }

    determineArchetype() {
        const roll = this.rng();
        if (roll < 0.2) return "Poltergeist"; // Chaotic, moves fast
        if (roll < 0.4) return "Wraith"; // Aggressive, follows mouse
        if (roll < 0.6) return "Phantom"; // Fades in and out
        if (roll < 0.8) return "Banshee"; // Loud audio visualization
        return "Shade"; // Passive, wanders background
    }

    generateBiography() {
        return {
            causeOfDeath: this.rand(MANIFEST_DATA.causesOfDeath),
            regret: this.rand(MANIFEST_DATA.regrets),
            manifestation: this.rand(MANIFEST_DATA.manifestations),
            lastWord: this.rand(MANIFEST_DATA.lastWords),
            alignment: this.rand(MANIFEST_DATA.alignments),
            deathDate: new Date(Date.now() - Math.floor(this.rng() * 10000000000)).toLocaleDateString()
        };
    }

    generateStats() {
        return {
            ectoplasmLevel: Math.floor(this.rng() * 100),
            hostility: Math.floor(this.rng() * 100),
            visibility: Math.floor(this.rng() * 100),
            frequency: 20 + Math.floor(this.rng() * 1000) // Audio frequency Hz
        };
    }
}
