    /**
     * Revert screen coordinates to canvas coordinates given the current CSS matrix transform.
     */
    static screenToCanvas(screenX, screenY, transformMatrix) {
        // matrix(scaleX, skewY, skewX, scaleY, translateX, translateY)
        const scale = transformMatrix.a || 1;
        const tx = transformMatrix.e || 0;
        const ty = transformMatrix.f || 0;

        return {
            x: (screenX - tx) / scale,
            y: (screenY - ty) / scale
        };
    }

    /**
     * Compute a cubic bezier curve path string for an SVG between two points.
     * Curvature determines the "sag" or control point distance.
     */
    static getBezierPath(startX, startY, endX, endY, curvature = 0.5) {
        // Compute distance for dynamic tangents
        const dx = endX - startX;
        // Standard blueprint-like routing (horizontal tangents)
        const tangentX = Math.abs(dx) * curvature + 50;

        const cp1x = startX + tangentX;
        const cp1y = startY;
        const cp2x = endX - tangentX;
        const cp2y = endY;

        return `M ${startX} ${startY} C ${cp1x} ${cp1y}, ${cp2x} ${cp2y}, ${endX} ${endY}`;
    }

    /**
     * Distance between two points
     */
    static distance(p1, p2) {
        return Math.hypot(p2.x - p1.x, p2.y - p1.y);
    }

    /**
     * Clamp a value between min and max
     */
    static clamp(val, min, max) {
        return Math.max(min, Math.min(max, val));
    }

